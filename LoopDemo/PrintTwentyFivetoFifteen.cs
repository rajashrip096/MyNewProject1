using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MyNewProject1.LoopDemo
{
    //write the code print 25 to 15.
    class PrintTwentyFivetoFifteen
    {
        static void Main(string[] args)
        {
            for(int i=25;i>=15;i--)
            {
                Console.WriteLine(i);
                Console.ReadLine();
            }
        }
    }
}
